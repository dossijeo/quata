create or replace function public.quata_chat_participant_after_change()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
    update public.chat_threads
    set updated_at = now()
    where id = coalesce(new.thread_id, old.thread_id);
    if tg_op = 'DELETE' then
        return old;
    end if;
    return new;
end;
$$;

