create or replace function public.quata_chat_auth_profile_id()
returns uuid
language sql
stable
security definer
set search_path = public, auth
as $$
    select cp.id
    from public.community_profiles cp
    where auth.uid() is not null
      and (cp.id = auth.uid() or cp.auth_user_id = auth.uid())
    limit 1
$$;

create or replace function public.quata_chat_actor_profile_id(p_actor_profile_id uuid default null)
returns uuid
language plpgsql
stable
security definer
set search_path = public, auth
as $$
declare
    v_auth_profile_id uuid;
begin
    v_auth_profile_id := public.quata_chat_auth_profile_id();

    if v_auth_profile_id is not null then
        if p_actor_profile_id is not null and p_actor_profile_id <> v_auth_profile_id then
            raise exception 'actor profile does not match authenticated Supabase user'
                using errcode = '42501';
        end if;
        return v_auth_profile_id;
    end if;

    -- Compatibility path for the current Android client, which stores
    -- AuthSession.token = 'supabase-profile:<community_profiles.id>' and
    -- calls Supabase with the anon key.
    if p_actor_profile_id is not null then
        if exists (select 1 from public.community_profiles where id = p_actor_profile_id) then
            return p_actor_profile_id;
        end if;
        raise exception 'actor profile does not exist' using errcode = '42501';
    end if;

    raise exception 'actor profile is required' using errcode = '42501';
end;
$$;

create or replace function public.quata_chat_is_thread_participant(
    p_thread_id bigint,
    p_profile_id uuid
)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
    select exists (
        select 1
        from public.chat_participants p
        where p.thread_id = p_thread_id
          and p.profile_id = p_profile_id
          and p.left_at is null
    )
$$;

create or replace function public.quata_chat_can_moderate(
    p_thread_id bigint,
    p_profile_id uuid
)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
    select exists (
        select 1
        from public.chat_participants p
        where p.thread_id = p_thread_id
          and p.profile_id = p_profile_id
          and p.left_at is null
          and p.role in ('owner', 'moderator')
    )
$$;

create or replace function public.quata_chat_is_blocked(
    p_thread_id bigint,
    p_sender_profile_id uuid,
    p_target_profile_id uuid
)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
    select exists (
        select 1
        from public.chat_profile_blocks b
        where b.blocker_profile_id = p_target_profile_id
          and b.blocked_profile_id = p_sender_profile_id
          and (b.thread_id is null or b.thread_id = p_thread_id)
    )
$$;

