create or replace function public.quata_chat_touch_updated_at()
returns trigger
language plpgsql
as $$
begin
    new.updated_at = now();
    return new;
end;
$$;

drop trigger if exists chat_threads_touch_updated_at on public.chat_threads;
create trigger chat_threads_touch_updated_at
before update on public.chat_threads
for each row
execute function public.quata_chat_touch_updated_at();

create or replace function public.quata_chat_refresh_thread_summary(p_thread_id bigint)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
    v_last record;
begin
    select m.created_at, left(coalesce(m.body, ''), 500) as preview
    into v_last
    from public.chat_messages m
    where m.thread_id = p_thread_id
      and m.deleted_at is null
    order by m.created_at desc, m.id desc
    limit 1;

    update public.chat_threads t
    set last_message_at = v_last.created_at,
        last_message_preview = coalesce(v_last.preview, ''),
        updated_at = now()
    where t.id = p_thread_id;
end;
$$;

create or replace function public.quata_chat_message_after_change()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
    v_thread_id bigint;
begin
    v_thread_id = coalesce(new.thread_id, old.thread_id);
    perform public.quata_chat_refresh_thread_summary(v_thread_id);
    if tg_op = 'DELETE' then
        return old;
    end if;
    return new;
end;
$$;

