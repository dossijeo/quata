-- Quata chat schema for replacing Better Messages with Supabase.
-- Identity model:
-- - The app currently treats public.community_profiles.id as the logged-in user id.
-- - If Supabase Auth is wired later, fill community_profiles.auth_user_id with auth.users.id.
-- - RPC functions in the next migration accept p_actor_profile_id for current app compatibility.

create extension if not exists pgcrypto;

alter table public.community_profiles
    add column if not exists auth_user_id uuid;

do $$
begin
    if not exists (
        select 1
        from pg_constraint
        where conname = 'community_profiles_auth_user_id_fkey'
    ) then
        alter table public.community_profiles
            add constraint community_profiles_auth_user_id_fkey
            foreign key (auth_user_id) references auth.users(id) on delete set null;
    end if;
end $$;

create unique index if not exists community_profiles_auth_user_id_key
    on public.community_profiles(auth_user_id)
    where auth_user_id is not null;

create table if not exists public.chat_threads (
    id bigint generated by default as identity primary key,
    type text not null default 'private',
    subject text,
    title text,
    image_url text,
    community_id uuid references public.community_walls(id) on delete set null,
    created_by_profile_id uuid not null references public.community_profiles(id) on delete cascade,
    unique_key text,
    allow_invite boolean not null default true,
    metadata jsonb not null default '{}'::jsonb,
    last_message_at timestamptz,
    last_message_preview text not null default '',
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    deleted_at timestamptz,
    constraint chat_threads_type_check
        check (type in ('private', 'group', 'wall', 'sos'))
);

create unique index if not exists chat_threads_unique_key_idx
    on public.chat_threads(unique_key)
    where unique_key is not null;

create index if not exists chat_threads_community_id_idx on public.chat_threads(community_id);
create index if not exists chat_threads_last_message_at_idx on public.chat_threads(last_message_at desc nulls last);
create index if not exists chat_threads_updated_at_idx on public.chat_threads(updated_at desc);

create table if not exists public.chat_private_threads (
    thread_id bigint primary key references public.chat_threads(id) on delete cascade,
    profile_low_id uuid not null references public.community_profiles(id) on delete cascade,
    profile_high_id uuid not null references public.community_profiles(id) on delete cascade,
    created_at timestamptz not null default now(),
    constraint chat_private_threads_distinct_profiles_check check (profile_low_id <> profile_high_id),
    constraint chat_private_threads_order_check check (profile_low_id < profile_high_id),
    constraint chat_private_threads_pair_key unique (profile_low_id, profile_high_id)
);

create table if not exists public.chat_participants (
    thread_id bigint not null references public.chat_threads(id) on delete cascade,
    profile_id uuid not null references public.community_profiles(id) on delete cascade,
    role text not null default 'member',
    joined_at timestamptz not null default now(),
    left_at timestamptz,
    is_hidden boolean not null default false,
    is_deleted boolean not null default false,
    muted_at timestamptz,
    pinned_at timestamptz,
    last_read_at timestamptz,
    last_read_message_id bigint,
    metadata jsonb not null default '{}'::jsonb,
    primary key (thread_id, profile_id),
    constraint chat_participants_role_check check (role in ('owner', 'moderator', 'member'))
);

create index if not exists chat_participants_profile_idx
    on public.chat_participants(profile_id, is_deleted, is_hidden, left_at);
create index if not exists chat_participants_thread_idx on public.chat_participants(thread_id);

create table if not exists public.chat_messages (
    id bigint generated by default as identity primary key,
    thread_id bigint not null references public.chat_threads(id) on delete cascade,
    sender_profile_id uuid not null references public.community_profiles(id) on delete cascade,
    body text not null default '',
    client_message_id text,
    reply_to_message_id bigint references public.chat_messages(id) on delete set null,
    forwarded_from_message_id bigint references public.chat_messages(id) on delete set null,
    forwarded_from_profile_id uuid references public.community_profiles(id) on delete set null,
    metadata jsonb not null default '{}'::jsonb,
    created_at timestamptz not null default now(),
    updated_at timestamptz,
    edited_at timestamptz,
    deleted_at timestamptz,
    deleted_by_profile_id uuid references public.community_profiles(id) on delete set null
);

create index if not exists chat_messages_thread_created_idx
    on public.chat_messages(thread_id, created_at, id);
create index if not exists chat_messages_thread_updated_idx
    on public.chat_messages(thread_id, coalesce(updated_at, edited_at, created_at) desc);
create index if not exists chat_messages_sender_idx on public.chat_messages(sender_profile_id);
create index if not exists chat_messages_reply_idx on public.chat_messages(reply_to_message_id);
create unique index if not exists chat_messages_client_message_unique_idx
    on public.chat_messages(thread_id, sender_profile_id, client_message_id)
    where client_message_id is not null;

